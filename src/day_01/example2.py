# This is an example of a simple program that checks if a number is even or odd.
# It takes user input, performs a modulus operation, and prints the result.
# The code is straightforward and easy to understand.
# It demonstrates basic input handling, conditional statements, and arithmetic operations in Python.

num = int(input("Enter a number: "))
if num % 2 == 0:
    print("Even")
else:
    print("Odd")

